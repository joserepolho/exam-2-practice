#include <unistd.h>

void	s_n_r(char *s, char c1, char c2)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c1)
			s[i] = c2;
		write(1, &s[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	if (ac == 4 && av[2][1] == '\0' && av[3][1] == '\0')
		s_n_r(av[1], av[2][0], av[3][0]);
	write(1, "\n", 1);
	return (0);
}

