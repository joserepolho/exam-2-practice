#include <unistd.h>

void	first_word(char *s)
{
	int	i;

	i = 0;
	while ((s[i] == 32 || (s[i] >= 8 && s[i] <= 11)) && s[i])
			i++;
	while (!(s[i] == 32 || (s[i] >= 8 && s[i] <= 11)) && s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
}

int	main(int ac, char **argv)
{
	if (ac == 2)
		first_word(argv[1]);
	write(1, "\n", 1);
}
