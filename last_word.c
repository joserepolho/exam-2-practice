#include <unistd.h>

void	last_word(char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	i--;
	while ((s[i] >= 9 && s[i] <= 13) || s[i] == 32)
		i--;
	if (i > -1)
	{
		while (!((s[i] >= 9 && s[i] <= 13) || s[i] == 32))
			i--;
		i++;
		while (s[i] > 32 && s[i] < 127)
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}

int	main(int ac, char **av)
{
	if (ac == 2)
		last_word(av[1]);
	write(1, "\n", 1);
	return (0);
}
