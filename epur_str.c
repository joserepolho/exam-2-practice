#include <unistd.h>

void	epur_str(char *s)
{
	int	i;

	i = 0;
	while ((s[i] >= 9 && s[i] <= 13) || s[i] == 32)
		i++;
	while (s[i])
	{
		while (s[i] > 32 && s[i] < 127)
		{
			write(1, &s[i], 1);
			i++;
		}
		while ((s[i] >= 9 && s[i] <= 13) || s[i] == 32)
		{
			i++;
			if (s[i] > 32 && s[i] < 127)
				write(1, " ", 1);
		}
	}
}


int	main(int ac, char **av)
{
	if (ac == 2)
		epur_str(av[1]);
	write(1, "\n", 1);
	return (0);
}
